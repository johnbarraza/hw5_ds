from __future__ import annotations

import io
import json
import requests
import pandas as pd
from fastmcp import FastMCP

CKAN_BASE = "https://datosabiertos.mef.gob.pe"

_SESSION = requests.Session()
_SESSION.headers.update({"User-Agent": "mef-efficiency-mcp/1.0"})

mcp = FastMCP("MEF Subnational Efficiency MCP")

def _ckan_get(action: str, params: dict) -> dict:
    url = f"{CKAN_BASE}/api/3/action/{action}"
    resp = _SESSION.get(url, params=params, timeout=30)
    resp.raise_for_status()
    data = resp.json()
    if not data.get("success"):
        raise RuntimeError(f"CKAN error for '{action}': {data.get('error')}")
    return data["result"]

