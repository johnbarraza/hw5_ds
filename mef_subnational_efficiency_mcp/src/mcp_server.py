@mcp.tool()
def inspeccionar_esquema_csv(url_csv: str, max_rows: int = 10):
    """Opens a partial stream of a CSV resource. Returns at most max_rows rows."""
    import io
    resp = _SESSION.get(url_csv, stream=True, timeout=60)
    resp.raise_for_status()
    contenido = b""
    for chunk in resp.iter_content(chunk_size=1024):
        contenido += chunk
        if len(contenido) > 500_000:
            break
    resp.close()
    df = pd.read_csv(io.BytesIO(contenido), encoding="utf-8-sig", on_bad_lines="skip", nrows=max_rows)
    return df


@mcp.tool()
def consultar_datastore_filtrado(resource_id: str, sql_or_filters: str) -> list[dict]:
    """Performs SQL-like queries on the CKAN datastore.

    Pass a SQL string (SELECT ...) or a JSON string of filter params.
    """
    query = sql_or_filters.strip()
    if query.upper().startswith("SELECT"):
        result = _ckan_get("datastore_search_sql", {"sql": query})
        return result.get("records", [])

    try:
        filters = json.loads(query) if query else {}
    except json.JSONDecodeError:
        filters = {}
    params = {"resource_id": resource_id, "limit": 100}
    params.update(filters)
    result = _ckan_get("datastore_search", params)
    return result.get("records", [])


@mcp.tool()
def obtener_ultimas_actualizaciones(limit: int = 10) -> list[dict]:
    """Returns recently changed packages on the portal."""
    result = _ckan_get("recently_changed_packages_activity_list", {"limit": limit})
    activities = result if isinstance(result, list) else []
    return [
        {
            "id": act.get("id", ""),
            "timestamp": act.get("timestamp", ""),
            "activity_type": act.get("activity_type", ""),
            "package_id": act.get("object_id", ""),
        }
        for act in activities[:limit]
    ]


@mcp.tool()
def listar_entidades_publicas() -> list[dict]:
    """Fetches active public organizations from the portal."""
    result = _ckan_get("organization_list", {"all_fields": True, "limit": 50})
    orgs = result if isinstance(result, list) else []
    return [
        {
            "id": org.get("id", ""),
            "name": org.get("name", ""),
            "title": org.get("title", ""),
            "package_count": org.get("package_count", 0),
        }
        for org in orgs
    ]


